#include <stdio.h>
#include <stdlib.h>

int main(void)
{
	printf( "%d\n", 'A'  );
	printf( "%d\n", 'a'  );
	printf( "%d\n", '&'  );
	printf( "%d\n", '$'  );
	printf( "%d\n", ' '  );
	printf( "%d\n", '+'  );
	printf( "%d\n", '/');
	printf( "%d\n", '~' );
	printf( "%d\n", '|' );
	return 0;
}
